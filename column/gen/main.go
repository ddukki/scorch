package main

import (
	"fmt"
	"os"
	"strings"
)

func main() {
	code := buildCode()
	if err := os.WriteFile("tuple_gen.go", []byte(code), 0644); err != nil {
		fmt.Fprintf(os.Stderr, "error: %v\n", err)
		os.Exit(1)
	}
}

func typeArgs(n int) string {
	args := make([]string, n)
	for i := 0; i < n; i++ {
		args[i] = fmt.Sprintf("T%d", i+1)
	}
	return strings.Join(args, ", ")
}

func buildCode() string {
	var b strings.Builder
	b.WriteString("// Code generated by go generate; DO NOT EDIT.\n")
	b.WriteString("// generate command: go run ./gen/\n\n")
	b.WriteString("package column\n\nimport \"github.com/ClickHouse/ch-go/proto\"\n\n")

	for n := 2; n <= 12; n++ {
		ta := typeArgs(n)

		// TupleNValue
		fmt.Fprintf(&b, "type Tuple%dValue[%s any] struct {\n", n, ta)
		for i := 0; i < n; i++ {
			fmt.Fprintf(&b, "\tT%d T%d\n", i+1, i+1)
		}
		b.WriteString("}\n\n")

		// TupleN
		fmt.Fprintf(&b, "type Tuple%d[%s any] struct {\n", n, ta)
		for i := 0; i < n; i++ {
			fmt.Fprintf(&b, "\tField%d Of[T%d]\n", i+1, i+1)
		}
		b.WriteString("}\n\n")

		// NewTupleN
		fmt.Fprintf(&b, "func NewTuple%d[%s any](", n, ta)
		for i := 0; i < n; i++ {
			if i > 0 {
				b.WriteString(", ")
			}
			fmt.Fprintf(&b, "col%d Of[T%d]", i+1, i+1)
		}
		fmt.Fprintf(&b, ") *Tuple%d[%s] {\n", n, ta)
		fmt.Fprintf(&b, "\treturn &Tuple%d[%s]{\n", n, ta)
		for i := 0; i < n; i++ {
			fmt.Fprintf(&b, "\t\tField%d: col%d,\n", i+1, i+1)
		}
		b.WriteString("\t}\n}\n\n")

		// Name
		fmt.Fprintf(&b, "func (t *Tuple%d[%s]) Name() string { return \"\" }\n\n", n, ta)

		// Len
		fmt.Fprintf(&b, "func (t *Tuple%d[%s]) Len() int { return t.Field1.Len() }\n\n", n, ta)

		// Type
		fmt.Fprintf(&b, "func (t *Tuple%d[%s]) Type() proto.ColumnType {\n", n, ta)
		fmt.Fprintf(&b, "\treturn proto.ColumnTypeTuple.Sub(")
		for i := 0; i < n; i++ {
			if i > 0 {
				b.WriteString(", ")
			}
			fmt.Fprintf(&b, "t.Field%d.Type()", i+1)
		}
		b.WriteString(")\n}\n\n")

		// Append
		fmt.Fprintf(&b, "func (t *Tuple%d[%s]) Append(v Tuple%dValue[%s]) {\n", n, ta, n, ta)
		for i := 0; i < n; i++ {
			fmt.Fprintf(&b, "\tt.Field%d.Append(v.T%d)\n", i+1, i+1)
		}
		b.WriteString("}\n\n")

		// AppendArr
		fmt.Fprintf(&b, "func (t *Tuple%d[%s]) AppendArr(v []Tuple%dValue[%s]) {\n", n, ta, n, ta)
		b.WriteString("\tfor _, x := range v {\n\t\tt.Append(x)\n\t}\n}\n\n")

		// Row
		fmt.Fprintf(&b, "func (t *Tuple%d[%s]) Row(i int) Tuple%dValue[%s] {\n", n, ta, n, ta)
		fmt.Fprintf(&b, "\treturn Tuple%dValue[%s]{\n", n, ta)
		for i := 0; i < n; i++ {
			fmt.Fprintf(&b, "\t\tT%d: t.Field%d.Row(i),\n", i+1, i+1)
		}
		b.WriteString("\t}\n}\n\n")

		// DecodeColumn
		fmt.Fprintf(&b, "func (t *Tuple%d[%s]) DecodeColumn(r *proto.Reader, rows int) error {\n", n, ta)
		for i := 0; i < n; i++ {
			fmt.Fprintf(&b, "\tif err := t.Field%d.DecodeColumn(r, rows); err != nil { return err }\n", i+1)
		}
		b.WriteString("\treturn nil\n}\n\n")

		// EncodeColumn
		fmt.Fprintf(&b, "func (t *Tuple%d[%s]) EncodeColumn(b *proto.Buffer) error {\n", n, ta)
		for i := 0; i < n; i++ {
			fmt.Fprintf(&b, "\tif err := t.Field%d.EncodeColumn(b); err != nil { return err }\n", i+1)
		}
		b.WriteString("\treturn nil\n}\n\n")
	}

	return b.String()
}
